/*
  Комнди SQL поділяється на певні "категорії" які називаються підмовами (sublanguages)

  DDL - Data Defenition Language - мова визначення даних
    використовується для опису структури даних
      команди розпочинаються з клчових слів CREATE, DROP, ALTER, TRUNCATE

  DML - Data Manipulation Lang - мова маніпуляції даних
    використовується для зміни даних у БД
      команди розпочинаються з клчових слів INSERT, UPDATE, DELETE
    
  DQL - Date Query Lang - мова запитів даних
    використовується для отримання даних з БД
    SELECT

  DCL - Data Contol Lang - мова контролю даних
    пов'язане з адмінстуруванням

  TCL - Transaction Control Lang - мова контролю транзакцій
*/